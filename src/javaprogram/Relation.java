package javaprogram;

		public class Relation {
			public static void main(String[] args) {
				int a = 11, b = 11;

				System.out.println(a==b);
				System.out.println(a!=b);
				System.out.println(a>b);
				System.out.println(a<b);
				System.out.println(a>=b);
				System.out.println(a<=b);
			}
		}